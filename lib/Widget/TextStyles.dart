import 'package:flutter/material.dart';

class TextStyles{
  static font14w4(Color color){
    return TextStyle(fontSize: 14, fontWeight: FontWeight.w400, color: color, fontFamily: 'Roboto');
  }

  static font15w4(Color color){
    return TextStyle(fontSize: 15, fontWeight: FontWeight.w400, color: color);
  }

  static font16w5(Color color){
    return TextStyle(fontSize: 16, fontWeight: FontWeight.w500, color: color);
  }

  static font18w7(Color color){
    return TextStyle(fontSize: 18, fontWeight: FontWeight.w700, color: color);
  }
}